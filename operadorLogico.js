let trueValue = true;
let falseValue = false;


//Operador Lógico - Conjunción: (y, and, &&)
console.log(`trueValue && trueValue: ${trueValue && trueValue}`)
console.log(`trueValue && falseValue: ${trueValue && falseValue}`)
console.log(`falseValue && trueValue: ${falseValue && trueValue}`)
console.log(`falseValue && falseValue: ${falseValue && falseValue}`)
console.log("")
//Operador Lógico - Disyunción: (o, or, ||)
console.log(`trueValue || trueValue: ${trueValue || trueValue}`)
console.log(`trueValue || falseValue: ${trueValue || falseValue}`)
console.log(`falseValue || trueValue: ${falseValue || trueValue}`)
console.log(`falseValue || falseValue: ${falseValue || falseValue}`)
console.log("")
//Operador Lógico - Negación: (no, not, !)
console.log(`!trueValue: ${!trueValue}`)
console.log(`!falseValue: ${!falseValue}`)
console.log("")